# The variable fahrenheit ask's the user to input the temperature in Fahrenheit
fahrenheit = float(input("Please Enter the Temperature in Fahrenheit: "))
                   
# The variable celsius uses the formula (F - 32) / 1.8 to convert Fahrenheit to Celsius
celsius = (fahrenheit - 32) / 1.8

# Print's the output temperature in Celsius
print("The Temperature in Celsius is: ", celsius)